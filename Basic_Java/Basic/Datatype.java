public class Datatype{
    public static void main(String[] args){
        byte a=5;
        short b=458;
        int c=45678;
        float d=45.2f;
        double e=4589.256;
        char f='f';
        boolean g=true;
        long h=4578921;
        System.out.print("byte: "+a+" Short:"+b+" Int:"+c+" Float:"+d+" double:"+e+" Char:"+f+" Boolean"+g+" Long:"+h);
    }
}